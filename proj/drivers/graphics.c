#define SCREEN_LO     *(char*) 0x8000
#define SCREEN_HI     *(char*) 0x8001
#define SCREEN_DATA   *(char*) 0x8002

#define NO_INT   *(char*) 0x7000

void hline(char x,char w,char y,char color) {
  const char color_byte = (color << 4) | color;
  char lo = x / 2;
  char i = 0;

  NO_INT = 1;
  SCREEN_HI = y / 2;
  if ( y % 2 == 1 ) lo += 128;
  if ( x % 2 == 1 ) {
    SCREEN_LO = lo;
    SCREEN_DATA = (SCREEN_DATA & 0xf0) | color;
    lo++;
    i = 1;
  }
  for ( ; i < w - 1; i += 2 ) {
    SCREEN_LO = lo;
    SCREEN_DATA = color_byte;
    lo++;
  }
  if ( (x % 2 == 1) ^ (w % 2 == 1) ) {
    SCREEN_LO = lo;
    SCREEN_DATA = (SCREEN_DATA & 0x0f) | (color << 4);
  }
  NO_INT = 0;
}

void krectangle(char x,char w,char y,char h,char color) {
  char i;
  for ( i = y; i < y + h; i++ ) {
    hline(x,w,i,color);
  }
}

void hreplace(char x,char w,char y,char to,char from) {
  const char to_color_byte = (to << 4) | to;
  const char from_color_byte = (from << 4) | from;
  char lo = x / 2;
  char i = 0;

  NO_INT = 1;
  SCREEN_HI = y / 2;
  if ( y % 2 == 1 ) lo += 128;
  if ( x % 2 == 1 ) {
    SCREEN_LO = lo;
    if ( SCREEN_DATA & 0x0f == from ) SCREEN_DATA = (SCREEN_DATA & 0xf0) | to;
    lo++;
    i = 1;
  }
  for ( ; i < w - 1; i += 2 ) {
    SCREEN_LO = lo;
    if ( SCREEN_DATA == from_color_byte ) SCREEN_DATA = to_color_byte;
    else if ( SCREEN_DATA == from ) SCREEN_DATA = (SCREEN_DATA & 0xf0) | to; // temp
    else if ( (SCREEN_DATA & 0xf0) >> 4 == from ) SCREEN_DATA = (SCREEN_DATA & 0x0f) | (to << 4);
    lo++;
  }
  if ( (x % 2 == 1) ^ (w % 2 == 1) ) {
    SCREEN_LO = lo;
    if ( SCREEN_DATA >> 4 == from ) SCREEN_DATA = (SCREEN_DATA & 0x0f) | (to << 4);
  }
  NO_INT = 0;
}

void kreplace(char x,char w,char y,char h,char to,char from) {
  char i;
  for ( i = y; i < y + h; i++ ) {
    hreplace(x,w,i,to,from);
  }
}

void kbitmap(const char* bitmap,char x,char y,char w,char h,char color0,char color1) {
  char lo,cur_y,high_four,low_four,i;
  char mat_byte = w * h / 8 - 1;
  char mat_bit = 7;

  NO_INT = 1;
  for ( cur_y = y; cur_y < y + h; cur_y++ ) {
    SCREEN_HI = cur_y / 2;
    lo = x / 2;
    if ( cur_y % 2 == 1 ) lo += 128;
    i = 0;
    if ( x % 2 == 1 ) {
      SCREEN_LO = lo;
      if ( (bitmap[mat_byte] >> mat_bit--) & 0x01 ) SCREEN_DATA = (SCREEN_DATA & 0xf0) | color1;
      else SCREEN_DATA = (SCREEN_DATA & 0xf0) | color0;
      if ( mat_bit == 0xff ) {
        mat_byte--;
        mat_bit = 7;
      }
      lo++;
      i = 1;
    }
    for ( ; i < w - 1; i += 2 ) {
      SCREEN_LO = lo;
      if ( (bitmap[mat_byte] >> mat_bit--) & 0x01 ) high_four = color1;
      else high_four = color0;
      if ( mat_bit == 0xff ) {
        mat_byte--;
        mat_bit = 7;
      }
      if ( (bitmap[mat_byte] >> mat_bit--) & 0x01 ) low_four = color1;
      else low_four = color0;
      if ( mat_bit == 0xff ) {
        mat_byte--;
        mat_bit = 7;
      }
      SCREEN_DATA = (high_four << 4) | low_four;
      lo++;
    }
    if ( (x % 2 == 1) ^ (w % 2 == 1) ) {
      SCREEN_LO = lo;
      if ( (bitmap[mat_byte] >> mat_bit--) & 0x01 ) SCREEN_DATA = (SCREEN_DATA & 0x0f) | (color1 << 4);
      else SCREEN_DATA = (SCREEN_DATA & 0x0f) | (color0 << 4);
      if ( mat_bit == 0xff ) {
        mat_byte--;
        mat_bit = 7;
      }
    }
  }
  NO_INT = 0;
}

